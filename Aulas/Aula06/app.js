/** operadores aritiméticos 
 * + soma / Concatenação
 * - subtração
 * * multiplicação
 * / divisão
 * ** potencia
 * % resto da divisão
 * */ 

let n1 = 10;
const n2 = 5;

console.log(n1 + n2); //15
console.log(n1 - n2); // 5
console.log(n1 * n2); // 50
console.log(n1 / n2); // 2
console.log(n1 ** n2); // 10000
console.log(n1 % n2);  // 0


/*
    operador de pos-incremento: VAR++
                decremento: VAR--
                pre-incremento: ++VAR
*/

console.log(n1++);  //10
console.log(++n1);  //12